package epam.homework.task1.basketball;

public class Ball {

	private String color;
	private int ballWeight;

	public Ball(String color, int ballWeight) {

		this.color = color;
		this.ballWeight = ballWeight;

	}

	public String getColor() {
		return color;
	}

	public int getBallWeight() {
		return ballWeight;
	}

}
